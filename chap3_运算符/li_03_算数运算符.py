# 标准算数运算符
print(1+1)
print(1-1)
print(1*1)
print(10/2)  # 5.0 除法运算，返回结果是float型数据
print(11//2)  # 5 整除运算，不改变数据类型

print('-----取余运算符-----')
print(11%2)  # 1

print('-----幂运算符-----')
print(2**3)  # 8

print('-----一正一负整除运算-----')
# 向下取整
print(9//-4)  # -3
print(-9//4)  # -3

print('-----一正一负取余运算-----')
# 使用公式：余数=被除数-除数*商
print(9%-4)  # -3 = 9-(-4)*(-3)
print(-9%4)  #  3 = -9-4*(-3)


